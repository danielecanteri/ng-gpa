package com.acme.xgpa.controllers;

public class Carico {

	private String job;
	private String data;
	private String ore;

	public void setJob(String job) {
		this.job = job;
	}

	public String getJob() {
		return job;
	}

	public void setData(String data) {
		this.data = data;
	}

	public String getData() {
		return data;
	}

	public void setOre(String ore) {
		this.ore = ore;
	}

	public String getOre() {
		return ore;
	}

}
